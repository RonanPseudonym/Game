int main() {
	Minerva::Engine engine;
	auto renderer = (Minerva::System::Renderer*) engine.AddSystem("renderer", new Minerva::System::Renderer());
	engine.AddSystem("noclip", new Minerva::System::Controller::Noclip());
	engine.Initialize();

	Minerva::Modeling::CustomModel model;
	model.AddMeshFromVertexTextureArray(cube, { "d.crate.png" }, renderer);

	renderer->RegisterModel("cube", &model);

	engine.AddPrototype("crate", Minerva::Prototype({
		{"transform", new Minerva::Component::Transform()},
		{"renderer",  new Minerva::Component::Renderer(
			"cube",
			"v.basic:f.basic",
			renderer
		)}
	}));

	engine("crate");

	unsigned int cam = renderer->InstantiateCamera(&engine);
	engine.AddComponent(cam, "noclip", new Minerva::Component::Noclip());

	engine.Cycle();

	return 0;
}